local function get_random_value(table_value)
	local values = {}
	for _, value in pairs(table_value) do
		table.insert(values, value)
	end

	local randomIndex = math.random(1, #values)
	return values[randomIndex]
end

---Returns random NeoVim logo
---@return table
function M.get_random_logo_func()
	return get_random_value(M.logos)
end

return M
